#include <iostream>

using namespace std;

int main() {
    int a = 13;
    int b = 4;

    cout << a + b << endl;  // 17
    cout << a - b << endl;  // 9
    cout << a * b << endl;  // 52
    cout << a / b << endl;  // 3
    cout << a % b << endl;  // 1
    cout << -a << endl;     // -13

    return 0;
}